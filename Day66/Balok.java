package Day66;

public class Balok {
    public static void main(String[] args) {
        int panjang = 20;
        int lebar = 5;
        int tinggi = 3;

        // Hitung volume balok
        int volume = panjang * lebar * tinggi;

        // Tampilkan hasil
        System.out.println("Volume balok: " + volume);
    }
    
}
