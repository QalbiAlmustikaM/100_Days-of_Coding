package Day77;

public class VolumeSilinder {
    public static void main(String[] args) {
        // Deklarasi variabel
        float r = 5;
        float h = 10;

        // Hitung volume silinder
        float volume = (float) (Math.PI * r * r * h);

        // Tampilkan volume silinder
        System.out.println("Volume silinder = " + volume);
    }
    
}
