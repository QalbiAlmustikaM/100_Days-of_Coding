package Day30;

public class TampilkanItemFor {
    static public void main(String[] args) {
        // Buatlah array item
        String[] items = {"Buku", "Pensil", "Penghapus", "Rak Buku", "Kursi"};

        // Tampilkan item dengan perulangan for
        for (int i = 0; i < items.length; i++) {
            System.out.println(items[i]);
        }
        
    }
    
}
