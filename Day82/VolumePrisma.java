package Day82;

public class VolumePrisma {
    public static void main(String[] args) {
        float alas = 5;
        float tinggi = 7;
        float panjang = 3;

        // Hitung volume
        float volume = (alas * tinggi * panjang) / 2;

        // Tampilkan volume
        System.out.println("Volume prisma dengan alas 5, tinggi 7, dan panjang 3 adalah " + volume);
    }
    
}
