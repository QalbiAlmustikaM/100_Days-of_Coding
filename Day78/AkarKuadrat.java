package Day78;

public class AkarKuadrat {
    public static void main(String[] args) {
        // Deklarasi variabel
        float x = 16;

        // Hitung akar kuadrat
        float akar = (float) Math.sqrt(x);

        // Tampilkan akar kuadrat
        System.out.println("Akar kuadrat dari " + x + " adalah " + akar);
    }
}

