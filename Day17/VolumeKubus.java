package Day17;

public class VolumeKubus {
    public static void main(String[] args) {
        // Deklarasi Variabel
        int sisi = 12;
        
        // Hitung Volume Kubus
        int volume = sisi * sisi * sisi;
        
        // Tampilkan
        System.out.println("Volume Kubus: " + volume);
    }
}
