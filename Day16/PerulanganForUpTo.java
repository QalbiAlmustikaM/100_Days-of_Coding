package Day16;

public class PerulanganForUpTo {
    public static void main(String[] args) {
        // Perulangan for up to untuk mencetak angka 1 hingga 6
        int awal = 1;
        int batas = 6;
        
        for (int i = awal; i <= batas; i++) {
            System.out.println("Iterasi ke-" + i);
        }
    }
    
}
