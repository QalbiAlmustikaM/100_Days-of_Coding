package Day39;

public class StringBufferDemo {
    public static void main(String[] args) {
        // Membuat objek StringBuffer
        StringBuffer sb = new StringBuffer("Halo, Qalbi!");

        // Menambahkan teks ke StringBuffer
        sb.append(" Selamat datang di java!");

        // Mencetak StringBuffer
        System.out.println(sb);
    }
    
}
