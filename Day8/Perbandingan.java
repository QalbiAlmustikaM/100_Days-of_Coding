package Day8;

public class Perbandingan {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        // Membandingkan dua bilangan bulat
        System.out.println("a > b: " + (a > b));
        System.out.println("a < b: " + (a < b));
        System.out.println("a == b: " + (a == b));
        System.out.println("a != b: " + (a != b));
        System.out.println("a >= b: " + (a >= b));
        System.out.println("a <= b: " + (a <= b));
    }
}
