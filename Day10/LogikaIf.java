package Day10;

public class LogikaIf {
    public static void main(String[] args) {
        // Logika IF
        int angka = 20;
        if (angka > 7){
            System.out.println("Angka Lebih Besar Dari 7");
        }else{
            System.out.println("Angka Tidak Lebih besar dari 7");
        }
    }
    
}
