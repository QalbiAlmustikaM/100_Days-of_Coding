package Day13;

public class PercabanganIfElseIfElse {
     public static void main(String[] args) {
        int bilangan = 10;
        
        if (bilangan > 10) {
            System.out.println("Bilangan tersebut lebih besar dari 10");
        } else if (bilangan > 7) {
            System.out.println("Bilangan tersebut lebih besar dari 7");
        } else {
            System.out.println("Bilangan tersebut tidak lebih dari 7");
        }
    }
    
}
