package Day85;

public class AkarPangkatDua {
    public static void main(String[] args) {
        int bilangan = 16;

        // Cari akar pangkat dua
        int akar = (int) Math.sqrt(bilangan);

        // Tampilkan akar pangkat dua
        System.out.println("Akar pangkat dua dari " + bilangan + " adalah: " + akar);
        
    }
}
