package Day68;

public class Tabung {
    public static void main(String[] args) {
         // Masukan jari-jari dan tinggi tabung
        int jari_jari = 5;
        int tinggi = 3;

        // Hitung volume tabung
        double volume = Math.PI * jari_jari * jari_jari * tinggi;

        // Tampilkan hasil
        System.out.println("Volume tabung: " + volume);
    }
    
}
